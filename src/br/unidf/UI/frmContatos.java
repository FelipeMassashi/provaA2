    private void btnAlterarConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarConActionPerformed
        // fazendo a validação dos dados, o método isEmpty que devolve true se a String for vazia ou false se a mesma nã estiver vazia
        if ((txtNomeCon.getText().isEmpty()) || (ftfTelefoneCon.getText().isEmpty()))
        {
            JOptionPane.showMessageDialog(null, "Verifique Se Tem Algum Campo Vazio !!!!");
            //O cursor do mouse vai se posicionado no campo Código do Contato
            txtCodigoIDCon.requestFocus();
        }
        else 
        {
            // instanciando a classe ContatosDTO do pacote DTO e criando seu objeto contato
            ContatoDTO contato = new ContatoDTO();
            // Instancia a classe ContatoDAL 
            ContatoDAL dal = new ContatoDAL();
            contato.setConID(Long.parseLong(txtCodigoIDCon.getText()));
            contato.setConNome(txtNomeCon.getText());
            //Mostra os dados que estão gravados na tabela Contatos
            contato.setConTel(ftfTelefoneCon.getText());
            try
            {
                // Chama o método alterarContato da Classe ContatoDAL
                dal.alterarContato(contato);
            }
            catch (Exception ex)
            {
                Logger.getLogger(frmContatos.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Contato " + contato.getConNome() + " Alterado Com Sucesso !!!!");
        }

    }//GEN-LAST:event_btnAlterarConActionPerformed

    private void btnExcluirConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirConActionPerformed
        //Verifica se os campos estão preenchidos    
        if (((txtNomeCon.getText().isEmpty()) || (ftfTelefoneCon.getText().isEmpty()) || (txtCodigoIDCon.getText().isEmpty()))) {
            JOptionPane.showMessageDialog(null, "Verifique Se Tem Algum Campo Vazio !!!!");
            txtCodigoIDCon.requestFocus();
        }
        else
        {
            // instanciando a classe contatosDTO do pacote DTO e criando seu objeto contato        
            ContatoDTO contato = new ContatoDTO();
            contato.setConID(Long.parseLong(txtCodigoIDCon.getText()));
            Object[] opcoes = {"Sim", "Não"};
            int contador = JOptionPane.showOptionDialog(null, "Deseja Excluir Este Contato: " + txtNomeCon.getText() + "?",
                    "Exclusão", JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[0]);
            if (contador == JOptionPane.YES_OPTION) {
                try
                {
                    ContatoDAL dal = new ContatoDAL();
                    dal.excluirContato(Integer.valueOf(txtCodigoIDCon.getText()));
                    JOptionPane.showMessageDialog(null, "Contato Excluído com Sucesso !!!!");
                    // apaga os dados preenchidos nos campos de texto abaixo
                    txtCodigoIDCon.setText(null);
                    txtNomeCon.setText("");
                    ftfTelefoneCon.setText(null);
                    txtCodigoIDCon.requestFocus();
                }
                catch (Exception ex)
                {
                    Logger.getLogger(frmContatos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_btnExcluirConActionPerformed

    private void btnMostrarTodosConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarTodosConActionPerformed
        //Limpa todas as caixas de texto
        txtCodigoIDCon.setText("");
        txtNomeCon.setText("");
        ftfTelefoneCon.setText(null);
        // instanciando a classe contatosDAL
        ContatoDAL contato = new ContatoDAL();
        //Cria a lista de todos os contatos dentro da tabela Contatos
        List<ContatoDTO> selecionaTodosConentes = new ArrayList<>();
        try
        {
            //Chama o método selecionaTodosContatos da Classe ContatosDAL e preeche
            //o mesmo com a lista de todos os contatops.
            selecionaTodosContatos = contato.selecionarListaContatos();
        }
        catch (Exception ex)
        {
            Logger.getLogger(frmContatos.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Prepara a tabela para receber os dados da busca(Lista)
        DefaultTableModel tabm = (DefaultTableModel)dgvContatos.getModel();
        for(int contador = tabm.getRowCount()-1; contador >=0; contador--)
        {
            tabm.removeRow(contador);
        }
        int contador = 0;
        for(ContatoDTO conatatos : selecionaTodosContatos)
        {
            //Mostra as informações dentro da Tabela
            tabm.addRow(new String[1]);
            dgvContatos.setValueAt(contatos.getConID(), contador, 0);
            dgvCcontatos.setValueAt(contatos.getConNome(),contador, 1);
            dgvContatos.setValueAt(contatos.getConTel(), contador, 2);        
            contador++;
        }
        //Limpa o campo Código do Contato
        txtCodigoIDCon.setText(null);
        //Posiciona o cursor do mouse no campo Nome do contato
        txtNomeCon.requestFocus();

    }//GEN-LAST:event_btnMostrarTodosConActionPerformed

