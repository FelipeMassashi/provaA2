    private void btnIncluirConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirConActionPerformed
        //Abrindo a conexão com o Banco de Dados
        ContatoDAL dal = new ContatoDAL();
        try
        {
            dal.abrirBD();
        }
        catch (Exception ex)
        {
            Logger.getLogger(frmContatos.class.getName()).log(Level.SEVERE, null, ex);
        }
        // instanciando a classe ContatoDTO do pacote DTO e criando seu objeto contato
        ContatoDTO conatato = new ContatoDTO();
        contato.setConNome(txtNomeCon.getText());
        contato.setConTel(ftfTelefoneCon.getText());
        // fazendo a validação dos dados
        if ((txtNomeCon.getText().isEmpty()) || (ftfTelefoneCon.getText().isEmpty()))
        {
            JOptionPane.showMessageDialog(null, "Os campos não podem estar vazios !!!!");
            txtNomeCon.requestFocus();
        }
        else
        {
            try {
                // Instancia a classe ContatoDAL e chama o método incluirContato
                dal.incluirContato(contato);
                JOptionPane.showMessageDialog(null, "Usuário " + txtNomeCon.getText() + " incluído com sucesso! ");
                dal.fecharBD();
                txtNomeCon.requestFocus();
                btnMostrarTodosCon.setEnabled(true);
            } catch (Exception ex)
            {
                Logger.getLogger(frmContatos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        // apaga os dados preenchidos nos campos de texto
        txtCodigoIDCon.setText("");
        txtNomeCon.setText("");
        ftfTelefoneCon.setText(null);

    }//GEN-LAST:event_btnIncluirConActionPerformed

